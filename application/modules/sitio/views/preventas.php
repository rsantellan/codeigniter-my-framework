  <h2>¿Como funciona una Preventa?</h2> 
        <p>
       Cuando un constructor o propietario inicia la construcción de una vivienda  y decide comenzar a venderla desde que aun se encuentra en sus cimientos, se dice que se trata de una "preventa", el  monto total que debe pagarse es mucho menor cuando el proyecto se encuentra aún en obra, que cuando está terminado por completo. De esta forma, el principal atractivo de la preventa es su precio.
        </p>
        <h2>¿Para que se hace la Preventa?</h2> 
        <p>
        
        Esta situación se debe a que los desarrolladores pueden financiar el proyecto con dinero que los compradores entregaron por anticipado y complementar su flujo de caja evitando costos financieros y trasladando esos beneficios al comprador. Por eso las “preventas” significan una gran oportunidad que buscan los inversores al momento de concretar el buen negocio.
        </p>
        <h2>Ventajas de comprar en Preventa</h2> 
        <p>
        <strong>Mayor Economía</strong> </br>
Hablando de términos económicos, es necesario mencionar la plusvalía. Es la principal ventaja, ya que no tiene que cubrir el importe total de la casa en un solo pago; al adquirir una propiedad en preventa, asegura Ud. un precio que por lo general aumenta durante el transcurso de la obra, el inmueble aumentará su valor conforme avance la construcción y, entre más demanda tenga, mayor será el precio que puede alcanzar.
La plusvalía aumenta entre 15 y 20% </br>
<strong>Estilo</strong> </br>
Otro de los beneficios de este sistema es que los futuros habitantes tienen la oportunidad de hacer modificaciones a su gusto y darle un toque personal a su hogar, frecuentemente puede seleccionar colores y tonos de sus paredes y en algunas ocasiones diferentes tipos de piso. </br>
<strong>Seguridad </strong></br>
Conocer la fecha y los términos de entrega te permite organizar a fondo la mudanza, los trámites legales y los gastos relacionados con este procedimiento. Además, te evita pérdidas de tiempo en cuando a búsqueda se refiere. </br>
<strong>Comodidad</strong> </br>
El saber que ya tiene seleccionada la vivienda de su gusto, le evita la pérdida de tiempo que implica buscar opciones cuando le urge moverse de lugar.        </p>
        <h2>¿Porque invertir en propiedades?</h2> 
        <p>
        
        A día de hoy existen pocas oportunidades de inversión y la gran mayoría de ellas son volátiles y de un alto nivel de riesgo. Las propiedades son una oportunidad de inversión altamente atractiva. Es una inversión tangible y usted puede capitalizar su dinero participando de proyectos prácticamente al costo donde ingresara al mismo en la etapa de prelanzamiento y podrá, una vez que el proceso de obra termine, alquilar o vender su unidad a un precio final de mercado.
        <h2>¿Como se compra en preventa?</h2> 
        <p>
         Es muy sencillo y a través de un contrato donde Ud. aporta una entrada  y va pagando la vivienda en plazos durante el transcurso de la obra, lo que le permite juntar capital para no tener que pagar todo al mismo tiempo.
        </p>
